package J25;

public class Calculator {

    public Calculator(){

    }

    public void add(int a, int b) {
        int suma = a + b;
        System.out.println(suma);
    }

    public void add(int a, int b, int c) {
        int suma = a + b + c;
        System.out.println(suma);
    }

    public void add(double a, double b) {
        double suma = a + b;
        System.out.println(suma);
    }

    public void add(double a, double b, double c) {
        double suma = a + b + c;
        System.out.println(suma);
    }


}
